package introduction.loops;

public class Whileloop {
    public static void main(String[] args) {
        /**************SYNTAX*************/
        /*
             while (condition) {
                   code block to be executed
                }
         */
        /***************EXAMPLE************/
int i = 0;
while (i < 5) {
  System.out.println(i);
  i++;
}
    }
}
